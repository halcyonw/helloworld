package shejimoshi.xiangyuanmoshi.bianjiqi;

public class WeizhiDaxiao {
    private int size;
    private int x;
    private int y;

    public WeizhiDaxiao() {
    }

    public WeizhiDaxiao(int size, int x, int y) {
        this.size = size;
        this.x = x;
        this.y = y;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
